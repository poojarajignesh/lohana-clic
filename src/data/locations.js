export const districts = Object.keys(
  locations.Gujarat
);

export function getTalukas(
  district
) {
  if (!district) return [];

  return Object.keys(
    locations.Gujarat[district] || {}
  );
}

export function getVillages(
  district, taluka) {
  if (!district ||!taluka) return [];

  return (
    locations.Gujarat[district]?.[taluka] || []
  );
}